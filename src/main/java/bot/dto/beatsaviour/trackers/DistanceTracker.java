package bot.dto.beatsaviour.trackers;

public class DistanceTracker {
    float rightSaber;
    float leftSaber;
    float rightHand;
    float leftHand;

    public float getRightSaber() {
        return rightSaber;
    }

    public void setRightSaber(float rightSaber) {
        this.rightSaber = rightSaber;
    }

    public float getLeftSaber() {
        return leftSaber;
    }

    public void setLeftSaber(float leftSaber) {
        this.leftSaber = leftSaber;
    }

    public float getRightHand() {
        return rightHand;
    }

    public void setRightHand(float rightHand) {
        this.rightHand = rightHand;
    }

    public float getLeftHand() {
        return leftHand;
    }

    public void setLeftHand(float leftHand) {
        this.leftHand = leftHand;
    }
}
